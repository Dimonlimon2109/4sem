    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
            this.Loaded += SetWindow;
        }

        private void SetWindow(object sender, RoutedEventArgs e)
        {
            if (this.DataContext is BaseVM baseVM)
            {
                baseVM.Window = this;
            }
        }
    }
